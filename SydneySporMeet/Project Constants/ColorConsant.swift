import Foundation
import UIKit

extension UIColor
{
    func getAppButtonColor() -> UIColor {
        return UIColor(red:0.043, green:0.576 ,blue:0.588 , alpha:1.00)
    }
    
    func getAppBackgroundColor() -> UIColor{
        return UIColor(red: 0.1, green: 0.2, blue: 0.3, alpha: 1)
    }
    
    func getAppTextColor() -> UIColor{
        return UIColor(red: 0.1, green: 0.2, blue: 0.3, alpha: 1)
    }
}
